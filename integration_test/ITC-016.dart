import 'package:flutter_dotenv/flutter_dotenv.dart';
import 'test_main.dart' as app;

extension PumpUntilFound on WidgetTester {
  Future<void> pumpUntilFound(Finder finder, {Duration timeout = const Duration(seconds: 10)}) async {
    final end = DateTime.now().add(timeout);
    while (DateTime.now().isBefore(end)) {
      await pump(const Duration(milliseconds: 100));
      if (finder.evaluate().isNotEmpty) return;
    }
    throw Exception('Widget not found within $timeout: $finder');
  }
}

Future<void> navigateBackToHome(WidgetTester tester, Key homeKey) async {
  int attempts = 0;
  while (tester.any(find.byKey(homeKey)) == false && attempts < 10) {
    bool wentBack = false;
    if (tester.any(find.byKey(const Key('backButton')))) {
      await tester.tap(find.byKey(const Key('backButton')));
      wentBack = true;
    }
    if (!wentBack) await tester.pageBack();
    await tester.pumpAndSettle();
    attempts++;
  }
  await tester.pumpUntilFound(find.byKey(homeKey));
}

Future<void> login(WidgetTester tester, String email, String password) async {
  await tester.pumpAndSettle();
  await tester.pumpUntilFound(find.byKey(const Key('login_email')));
  await tester.enterText(find.byKey(const Key('login_email')), email);
  await tester.enterText(find.byKey(const Key('login_password')), password);
  await tester.tap(find.byKey(const Key('login_button')));
  await tester.pumpAndSettle();
}

Future<void> logout(WidgetTester tester, Key homeKey) async {
  await navigateBackToHome(tester, homeKey);
  await tester.tap(find.byKey(const Key('drawer_button')));
  await tester.pumpAndSettle();
  await tester.tap(find.text('Logout'));
  await tester.pumpAndSettle();
  await tester.pumpUntilFound(find.byKey(const Key('login_email')));
}

void main() {
  IntegrationTestWidgetsFlutterBinding.ensureInitialized();

  setUpAll(() async {
    await dotenv.load(fileName: 'important_stuff.env');
    await Supabase.initialize(
      url: dotenv.env['SUPABASE_URL'] ?? '',
      anonKey: dotenv.env['SUPABASE_ANON_KEY'] ?? '',
    );
  });

  testWidgets('ITC-016: Counselor accepts appointment and student sees ACCEPTED status', (tester) async {
    await app.testMain();
    await tester.pumpAndSettle();

    // Create a test appointment via Supabase so the test has a stable target
    final supabase = Supabase.instance.client;
    // find counselor id and student id by user emails
    final studentUser = await supabase.from('users').select('user_id').eq('email', 'itzmethresh@gmail.com').maybeSingle();
    final counselorUser = await supabase.from('users').select('user_id').eq('email', 'allanjayv01@gmail.com').maybeSingle();
    if (studentUser == null || counselorUser == null) {
      throw Exception('Test users not found in DB');
    }
    final studentId = studentUser['user_id'];
    final counselorRow = await supabase.from('counselors').select('counselor_id, user_id').eq('user_id', counselorUser['user_id']).maybeSingle();
    final counselorId = counselorRow != null ? counselorRow['counselor_id'] : null;
    if (counselorId == null) throw Exception('Counselor profile not found');

    // Insert appointment for tomorrow, status pending
    final tomorrow = DateTime.now().add(const Duration(days: 1));
    final apptDate = '${tomorrow.year}-${tomorrow.month.toString().padLeft(2,'0')}-${tomorrow.day.toString().padLeft(2,'0')}';
    final startTime = DateTime(tomorrow.year, tomorrow.month, tomorrow.day, 10, 0).toIso8601String();
    final endTime = DateTime(tomorrow.year, tomorrow.month, tomorrow.day, 11, 0).toIso8601String();

    final insertResult = await supabase.from('counseling_appointments').insert({
      'user_id': studentId,
      'counselor_id': counselorId,
      'appointment_date': apptDate,
      'start_time': '${10.toString().padLeft(2,'0')}:00:00',
      'end_time': '${11.toString().padLeft(2,'0')}:00:00',
      'status': 'pending',
      'notes': 'ITC-016 autogenerated appointment',
    }).select().maybeSingle();

    if (insertResult == null) throw Exception('Failed to create test appointment');

    final appointmentId = insertResult['appointment_id'];

    // Counselor login
    await login(tester, 'allanjayv01@gmail.com', 'allan123');
    await tester.pumpUntilFound(find.byKey(const Key('counselorHomeScreen')));

    // Navigate to All Appointments
    Navigator.of(tester.element(find.byKey(const Key('counselorHomeScreen')))).pushNamed('/all-appointments');
    await tester.pumpAndSettle();

    // Wait for list and then try to find a PENDING card and tap its UPDATE
    await tester.pump(const Duration(seconds: 2));
    expect(find.text('PENDING'), findsWidgets);

    // Tap first 'UPDATE' button found
    final updateButton = find.text('UPDATE').first;
    await tester.tap(updateButton);
    await tester.pumpAndSettle();

    // Select 'Accept Appointment' menu item
    final acceptItem = find.text('Accept Appointment');
    await tester.tap(acceptItem);
    await tester.pumpAndSettle();

    // Confirmation dialog appears, enter a message and Update
    if (tester.any(find.byType(TextField))) {
      await tester.enterText(find.byType(TextField).last, 'Accepting for ITC-016');
      await tester.pumpAndSettle();
    }
    // Tap Update on dialog
    final updateConfirm = find.text('Update');
    if (tester.any(updateConfirm)) {
      await tester.tap(updateConfirm);
      await tester.pumpAndSettle();
    }

    // Wait and expect success snackbar
    await tester.pump(const Duration(seconds: 2));
    expect(find.textContaining('Appointment status updated', findRichText: false), findsWidgets);

    // Logout counselor
    await logout(tester, const Key('counselorHomeScreen'));

    // Student login to verify appointment status
    await login(tester, 'itzmethresh@gmail.com', 'allan123');
    await tester.pumpUntilFound(find.byKey(const Key('studentHomeScreen')));

    // Navigate to Appointments screen
    Navigator.of(tester.element(find.byKey(const Key('studentHomeScreen')))).pushNamed('/student-appointments');
    await tester.pumpAndSettle();

    // Wait and assert that 'ACCEPTED' appears somewhere in the appointments list
    await tester.pump(const Duration(seconds: 2));
    expect(find.text('ACCEPTED'), findsWidgets);

    // Cleanup: remove the test appointment (optional)
    try {
      await supabase.from('counseling_appointments').delete().eq('appointment_id', appointmentId);
    } catch (_) {}

    // Logout student
    await logout(tester, const Key('studentHomeScreen'));
  }, timeout: const Timeout(Duration(minutes: 4)));
}
